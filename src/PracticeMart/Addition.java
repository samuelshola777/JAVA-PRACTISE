package PracticeMart;

public class Addition {
    public int addTwoNum(int a, int b) {
      int num = a+b;
      return num;
    }
    public int multiply(int a, int b){
        int num = a * b;
        return num;
    }

    public int divide(int a, int b) {
        int num = a / b;
        return num;
    }
}
