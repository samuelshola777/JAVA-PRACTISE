package printLoopLesson;

public class printFIveNestedLoop {
    public static void main(String[] args) {
int n = 10;
        for (int i = 1; i <= n; i++) {
            for (int j = 1 ; j <= 20 ; j++) {
                System.out.print("+  ");
            }
            System.out.println();
        }

    }
}
