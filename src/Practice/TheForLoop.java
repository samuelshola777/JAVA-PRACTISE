package Practice;

public class TheForLoop {
    public static void main(String[] args) {
// so this will count from zero to nine
        for (int goat = 0;  goat < 10; goat ++ ){
            System.out.println(goat);
        }
// this will print from 1 to 10
for (int g = 10; g > 0; g --){
    System.out.println(g);
}
//this will print from 2 to 20
for (int s = 20; s > 0; s -= 2 ){
    System.out.println(s);

        }

    }
}
